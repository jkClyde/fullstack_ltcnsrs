function getRandomInitial() {
  const initials = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
  return initials.charAt(Math.floor(Math.random() * initials.length)) + '.';
}

const getRandomStatusWeight = () => {
  const statusWeightValues = ["SU", "U", "N", "OW"];
  return statusWeightValues[Math.floor(Math.random() * statusWeightValues.length)];
};

const getRandomStatusHeight = () => {
  const statusHeightValues = ["SST", "ST", "N", "T"];
  return statusHeightValues[Math.floor(Math.random() * statusHeightValues.length)];
};

